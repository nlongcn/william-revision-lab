const religionState = {
  activeTopicId: religionTopics[0].id,
  view: "single",
  search: "",
  expandedAll: false,
  answersRevealed: 0
};

const religionNav = document.querySelector("#religionNav");
const religionContainer = document.querySelector("#religionContainer");
const religionSearch = document.querySelector("#religionSearch");
const religionTopicsCount = document.querySelector("#religionTopicsCount");
const religionAnsweredCount = document.querySelector("#religionAnsweredCount");
const religionExpandAllButton = document.querySelector("#religionExpandAllButton");
const religionRandomButton = document.querySelector("#religionRandomButton");
const religionToggleButtons = document.querySelectorAll(".religion-toggle-button");

if (religionTopicsCount) religionTopicsCount.textContent = String(religionTopics.length);

renderReligionNav();
renderReligionTopics();

religionSearch.addEventListener("input", (event) => {
  religionState.search = event.target.value.trim().toLowerCase();
  if (religionState.search) religionState.view = "all";
  syncReligionToggleButtons();
  renderReligionNav();
  renderReligionTopics();
});

religionExpandAllButton.addEventListener("click", () => {
  religionState.expandedAll = !religionState.expandedAll;
  religionState.view = religionState.expandedAll ? "all" : "single";
  religionExpandAllButton.textContent = religionState.expandedAll ? "Focus view" : "Expand all";
  syncReligionToggleButtons();
  renderReligionTopics();
});

religionRandomButton.addEventListener("click", () => {
  const topic = pickReligion(religionTopics);
  religionState.activeTopicId = topic.id;
  religionState.view = "single";
  religionState.search = "";
  religionState.expandedAll = false;
  religionSearch.value = "";
  religionExpandAllButton.textContent = "Expand all";
  syncReligionToggleButtons();
  renderReligionNav();
  renderReligionTopics();
  document.querySelector("#religionPanel .content-stage").scrollIntoView({ behavior: "smooth", block: "start" });
});

religionToggleButtons.forEach((button) => {
  button.addEventListener("click", () => {
    religionState.view = button.dataset.religionView;
    religionState.expandedAll = religionState.view === "all";
    religionExpandAllButton.textContent = religionState.expandedAll ? "Focus view" : "Expand all";
    syncReligionToggleButtons();
    renderReligionTopics();
  });
});

function renderReligionNav() {
  religionNav.innerHTML = "";
  const filtered = filteredReligionTopics();

  filtered.forEach((topic, index) => {
    const button = document.createElement("button");
    button.className = "topic-nav-button religion-nav-button";
    if (topic.id === religionState.activeTopicId) button.classList.add("active");
    button.type = "button";
    button.innerHTML = `<strong>${index + 1}. ${topic.title}</strong><span>${topic.tag}</span>`;
    button.addEventListener("click", () => {
      religionState.activeTopicId = topic.id;
      religionState.view = "single";
      religionState.search = "";
      religionState.expandedAll = false;
      religionSearch.value = "";
      religionExpandAllButton.textContent = "Expand all";
      syncReligionToggleButtons();
      renderReligionNav();
      renderReligionTopics();
      document.querySelector("#religionPanel .content-stage").scrollIntoView({ behavior: "smooth", block: "start" });
    });
    religionNav.append(button);
  });
}

function renderReligionTopics() {
  religionContainer.innerHTML = "";
  const visibleTopics =
    religionState.view === "all"
      ? filteredReligionTopics()
      : religionTopics.filter((topic) => topic.id === religionState.activeTopicId);

  if (!visibleTopics.length) {
    const empty = document.createElement("div");
    empty.className = "empty-state";
    empty.textContent = "No lesson chapter matches that search. Try Tzedakah, equality, dignity, service or compassion.";
    religionContainer.append(empty);
    return;
  }

  visibleTopics.forEach((topic) => {
    religionContainer.append(createReligionTopicCard(topic));
  });
}

function createReligionTopicCard(topic) {
  const article = document.createElement("article");
  article.className = "topic-card religion-topic-card";
  article.id = `religion-${topic.id}`;

  const header = document.createElement("div");
  header.className = "topic-card-header religion-card-header";
  const headerText = document.createElement("div");
  const kicker = document.createElement("p");
  kicker.className = "topic-kicker";
  kicker.textContent = topic.tag;
  const title = document.createElement("h2");
  title.textContent = topic.title;
  headerText.append(kicker, title);
  const practiceButton = document.createElement("button");
  practiceButton.className = "mini-button";
  practiceButton.type = "button";
  practiceButton.textContent = "Reflect";
  practiceButton.addEventListener("click", () => {
    article.querySelector(".practice-block").scrollIntoView({ behavior: "smooth", block: "start" });
  });
  header.append(headerText, practiceButton);

  const story = document.createElement("section");
  story.className = "religion-story-block";
  const image = document.createElement("img");
  image.src = topic.image;
  image.alt = topic.imageAlt;
  image.loading = "lazy";
  const storyCopy = document.createElement("div");
  storyCopy.className = "religion-story-copy";
  const storyLabel = document.createElement("p");
  storyLabel.className = "visual-label";
  storyLabel.textContent = "Story scene";
  const narrative = document.createElement("p");
  narrative.className = "religion-narrative";
  narrative.textContent = topic.narrative;
  storyCopy.append(storyLabel, narrative);
  story.append(image, storyCopy);

  const infoGrid = document.createElement("div");
  infoGrid.className = "info-grid religion-info-grid";
  infoGrid.append(
    makeReligionInfoBlock("religion-core", "Core idea", topic.idea),
    makeReligionInfoBlock("religion-remember", "Remember", topic.remember),
    makeReligionInfoBlock("religion-activity", topic.activity.title, topic.activity.prompt)
  );

  article.append(header, story, infoGrid);

  if (topic.decisions) {
    const decisions = document.createElement("section");
    decisions.className = "religion-decision-block";
    const decisionHeading = document.createElement("div");
    const label = document.createElement("p");
    label.className = "visual-label";
    label.textContent = "Decision lab";
    const decisionTitle = document.createElement("h3");
    decisionTitle.textContent = "Which action protects dignity?";
    decisionHeading.append(label, decisionTitle);
    const decisionGrid = document.createElement("div");
    decisionGrid.className = "decision-grid";
    topic.decisions.forEach((decision) => decisionGrid.append(createDecisionCard(decision)));
    decisions.append(decisionHeading, decisionGrid);
    article.append(decisions);
  }

  if (topic.writingFrame) {
    const frame = document.createElement("section");
    frame.className = "religion-writing-block";
    const label = document.createElement("p");
    label.className = "visual-label";
    label.textContent = "Writing frame";
    const heading = document.createElement("h3");
    heading.textContent = "Turn ideas into a reflection";
    const list = document.createElement("ol");
    topic.writingFrame.forEach((sentence) => {
      const item = document.createElement("li");
      item.textContent = sentence;
      list.append(item);
    });
    frame.append(label, heading, list);
    article.append(frame);
  }

  const practice = document.createElement("section");
  practice.className = "practice-block";
  const heading = document.createElement("div");
  heading.className = "practice-heading";
  const headingCopy = document.createElement("div");
  const practiceTitle = document.createElement("h3");
  practiceTitle.textContent = "Reflect and reveal";
  const practiceIntro = document.createElement("p");
  practiceIntro.textContent = "Answer out loud or write a sentence first, then reveal the model response.";
  headingCopy.append(practiceTitle, practiceIntro);
  const moreButton = document.createElement("button");
  moreButton.className = "primary-button";
  moreButton.type = "button";
  moreButton.textContent = "Mix questions";
  const list = document.createElement("ol");
  list.className = "questions-list";
  renderReligionQuestions(list, topic.questions);
  moreButton.addEventListener("click", () => {
    renderReligionQuestions(list, shuffleReligion(topic.questions));
  });
  heading.append(headingCopy, moreButton);
  practice.append(heading, list);
  article.append(practice);

  return article;
}

function makeReligionInfoBlock(className, title, content) {
  const block = document.createElement("section");
  block.className = `info-block ${className}`;
  const heading = document.createElement("h3");
  heading.textContent = title;
  block.append(heading);

  if (Array.isArray(content)) {
    const list = document.createElement("ul");
    content.forEach((text) => {
      const item = document.createElement("li");
      item.textContent = text;
      list.append(item);
    });
    block.append(list);
  } else {
    const paragraph = document.createElement("p");
    paragraph.textContent = content;
    block.append(paragraph);
  }

  return block;
}

function createDecisionCard(decision) {
  const card = document.createElement("article");
  card.className = "decision-card";
  const scenario = document.createElement("h4");
  scenario.textContent = decision.scenario;
  const weak = document.createElement("p");
  weak.innerHTML = `<strong>Quick help:</strong> ${decision.weak}`;
  const stronger = document.createElement("p");
  stronger.innerHTML = `<strong>Dignity help:</strong> ${decision.stronger}`;
  const reason = document.createElement("p");
  reason.className = "decision-reason";
  reason.textContent = decision.reason;
  card.append(scenario, weak, stronger, reason);
  return card;
}

function renderReligionQuestions(target, questions) {
  target.innerHTML = "";
  questions.forEach(({ q, a, reflect }) => {
    const item = document.createElement("li");
    item.className = "question-card religion-question-card";

    const question = document.createElement("p");
    question.className = "question-text";
    question.textContent = q;

    const button = document.createElement("button");
    button.className = "answer-button";
    button.type = "button";
    button.textContent = "Reveal response";
    button.addEventListener("click", () => {
      const wasRevealed = item.classList.contains("revealed");
      item.classList.toggle("revealed");
      button.textContent = item.classList.contains("revealed") ? "Hide response" : "Reveal response";
      if (!wasRevealed && !item.dataset.counted) {
        item.dataset.counted = "true";
        religionState.answersRevealed += 1;
        religionAnsweredCount.textContent = String(religionState.answersRevealed);
      }
    });

    const answer = document.createElement("p");
    answer.className = "answer-text";
    answer.textContent = `Model response: ${a}`;

    const reflection = document.createElement("p");
    reflection.className = "answer-fact";
    reflection.textContent = `Reflection move: ${reflect}`;

    item.append(question, button, answer, reflection);
    target.append(item);
  });
}

function filteredReligionTopics() {
  if (!religionState.search) return religionTopics;
  return religionTopics.filter((topic) => {
    const decisionText = (topic.decisions || [])
      .map((decision) => Object.values(decision).join(" "))
      .join(" ");
    const questionText = topic.questions.map((question) => `${question.q} ${question.a} ${question.reflect}`).join(" ");
    const writingText = (topic.writingFrame || []).join(" ");
    const haystack = [
      topic.title,
      topic.tag,
      topic.narrative,
      topic.idea,
      topic.activity.title,
      topic.activity.prompt,
      ...topic.remember,
      decisionText,
      writingText,
      questionText
    ]
      .join(" ")
      .toLowerCase();
    return haystack.includes(religionState.search);
  });
}

function syncReligionToggleButtons() {
  religionToggleButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.religionView === religionState.view);
  });
}

function pickReligion(items) {
  return items[Math.floor(Math.random() * items.length)];
}

function shuffleReligion(items) {
  return [...items].sort(() => Math.random() - 0.5);
}
